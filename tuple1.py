# A) WAP to count the number of students with the “A” grade in the following tuple.
#  (“C”, “D” , “A” , “A” , “B” , “B” , “A”)

# Create the tuple
grades = ("C", "D", "A", "A", "B", "B", "A")

# Count number of "A" grades
count_A = grades.count("A")

# Display result
print("Number of students with grade A:", count_A)